                                    <h3>01. Air or Gas Embolism</h3>
                                    <h2>Hyperbaric Oxygen Therapy Indications: Air or Gas Embolism Richard E. Moon</h2>
                                    <span>Rationale</span>
                                    <p>Gas embolism occurs when gas bubbles enter arteries or veins. Arterial gas embolism (AGE) was classically described during submarine escape training, in which pulmonary barotrauma occurred during free ascent after breathing compressed gas at depth. Pulmonary barotrauma and gas embolism due to breath holding can occur after an ascent of as little as one meter.(1) AGE has been attributed to normal ascent in divers with lung pathology such as bullous disease and asthma.(2,3) Pulmonary barotrauma can also occur as a result of blast injury in or out of water, (4,5) mechanical ventilation,(6) penetrating chest trauma,(7) chest tube placement(8) and bronchoscopy.(9)</p>
                                    <p>Venous gas embolism (VGE) occurs commonly after compressed gas diving.(10,11) Normally, VGE bubbles are trapped by the pulmonary capillaries and do not cause clinical symptoms. However, in large volumes, VGE can cause cough, dyspnea and pulmonary edema,(12,13) and may overwhelm the capacity of the pulmonary capillary network, allowing bubbles to enter the arterial circulation.(14,15) VGE can also enter the left heart directly via an atrial septal defect or patent foramen ovale(16-19)</p>
                                    <p>Causes of gas embolism other than diving include accidental intravenous air injection,(20,21)cardiopulmonary bypass accidents,(22) needle biopsy of the lung,(23) hemodialysis,(24) central venous catheter placement or disconnection,(25,26) gastrointestinal endoscopy,(27) hydrogen peroxide irrigation(28-30) or ingestion,(31-33) arthroscopy,(34,35) cardiopulmonary resuscitation,(36) percutaneous hepatic puncture,(37) blowing air into the vagina during orogenital sex(38-40) and sexual intercourse after childbirth.(41) Air embolism can occur during procedures in which the surgical site is under pressure (e.g. laparoscopy,(42-46) transurethral surgery,(47,48) vitrectomy,(49) endoscopic vein harvesting(50) and hysteroscopy(51,52)). Massive VGE can occur due to passive entry of air into surgical wounds that are elevated above the level of the heart (such that the pressure in adjacent veins is subatmospheric).(53) This has classically been described in sitting craniotomy,(54) but has also occurred during cesarean section,(55)prostatectomy using the radical perineal(56) and retropubic(57,58) approaches, spine surgery,(59,60) hip replacement,(61) liver resection,(62) liver transplantation(63) and insertion of dental implants.(64,65)</p>
                                    <p>Clinical deficits can occur after intra-arterial injection of only small volumes of air. Intravenous injection is often asymptomatic. Injection of up to 0.5-1 mL/kg has been tolerated in experimental animals.(66) In humans, continuous IV infusion of oxygen at 10 mL/min has been reported as well tolerated, while 20 mL/min caused symptoms.(67) Compared with constant infusions, injections of air are more likely to cause clinical abnormalities.(68)</p>
