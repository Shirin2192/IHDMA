                        <h6>Scientific Definition of Hyperbaric Oxygen (HBO2) Therapy</h6>
                        <p>Hyperbaric oxygen is a medical procedure requiring a physician’s prescription and oversight.
                            All patients must have their entire body placed within a hard sided hyperbaric chamber that
                            meets the American Society of Mechanical Engineers and Pressure Vessels for Human Occupancy
                            (ASME-PVHO-1) code, and the National Fire Protection Agency (NFPA 99) code and standards for
                            hyperbaric chambers, at a pressure of not less than 2.0 ATA (202.65 KPa) while breathing
                            physician prescribed medical grade oxygen for an amount of time that is typically between
                            90-120 minutes per treatment. Medical grade oxygen (>99.0% oxygen purity) is the only
                            acceptable gas that should be used for therapeutic delivery of hyperbaric oxygen.*</p>
                        <p style="color:red"> * Medical grade oxygen should meet United States Pharmacopeia (USP) or
                            national equivalent standard for purity.</p>

                        <h6>Safe Delivery and Proven Hyperbaric Oxygen Applications</h6>
                        <p>Scientifically supported hyperbaric treatments are usually delivered at pressures between 1.9
                            to 3.0 ATA.** HBO2 therapy is a standard of care for many medical conditions including
                            decompression sickness, carbon monoxide poisoning, diabetic wounds, delayed radiation
                            injury, necrotizing fasciitis, gas gangrene, refractory osteomyelitis, and several other
                            conditions proven by peer-reviewed research. Treatment chambers should be designed,
                            constructed, operated and certified to the standards established by the NFPA (National Fire
                            Protection Association) and ASME PVHO-1 (American Society of Mechanical Engineers-Safety
                            Standard for Pressure Vessels for Human Occupancy) or other internationally equivalent
                            regulatory agencies. The Undersea and Hyperbaric Medical Society (UHMS) has established
                            criteria for accreditation of hyperbaric treatment facilities designed to ensure safe and
                            clinically appropriate treatments. Most disorders require a series of treatments delivered
                            daily for several weeks. These treatments should be prescribed and supervised by qualified
                            physicians with appropriate training.</p>
                        <p>**Hyperbaric oxygen has been under study for traumatic brain injury in several randomized
                            controlled trials. Although one of these studies is a positive trial at a treatment pressure
                            of 1.5 ATA, such treatments are at this time considered investigational.</p>
                        <h6>Unproven Hyperbaric Treatment (Often Termed “Mild Hyperbaric Oxygen”)</h6>
                        <p>Hyperbaric treatment at minimally elevated chamber pressures (mild hyperbaric oxygen) is
                            unproven. Mild hyperbaric oxygen therapy is currently considered to be exposures delivered
                            at pressures lower than 1.5 ATA. Most clients in “mild hyperbaric chambers” receive
                            breathing gas mixes well less than 95% O2, often delivered through breathing devices such as
                            masks that do not provide a tight seal and by the nature of their construction allow mixing
                            of gases with the ambient chamber air, further reducing the oxygen concentration.
                            Unfortunately, these treatments have become widely available in so-called “wellness centers”
                            and health spas outside the setting of medical facilities, including physicians’ offices.
                            Generally, these treatments are not physician-prescribed or supervised. The recent interest
                            in and commercial growth of these treatments has led to the use of unsafe and unapproved
                            chamber vessels outside medical facilities and often in commercial properties in malls or
                            shopping centers. These facilities often operate without the appropriate adherence to fire
                            safety and chamber construction standards, putting those exposed at risk for serious injury
                            and even death. These facilities typically deliver sessions in these low-pressure vessels
                            for a spectrum of medical disorders or complaints, including those for which standard
                            hyperbaric medicine has been found to be effective, but also including disorders for which
                            there is no scientific proof for any type of hyperbaric oxygen treatments.</p>
